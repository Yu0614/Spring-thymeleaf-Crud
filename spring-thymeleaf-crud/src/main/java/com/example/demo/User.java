package com.example.demo;


public class User {
	private int id;
	private String name;

	/*
	 *  id setter&getter
	 */
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}

	/*
	 *  name setter&getter
	 */
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;

	}




}
